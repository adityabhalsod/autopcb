"""AutoIC AI engine.

All Anthropic Claude API calls run inside `AIWorker(QThread)` — the Qt main
thread is never blocked. `AIEngine` holds the SDK client, model, and system
prompts and exposes one method per pipeline stage. Each method returns the
spawned worker; callers connect to its `progress / response_ready / error`
signals.

Defensive parsing:
- JSON tasks: extract first balanced ``{...}`` block, validate via ``json.loads``.
- HDL/SPICE tasks: extract the first triple-backtick code block; fall back to
  the entire response if no fence is present.
- Failures (API errors, parse errors) trigger up to 3 exponential-backoff
  retries (1s → 2s → 4s).
"""

from __future__ import annotations

import json
import logging
import re
import time
import traceback
from dataclasses import dataclass
from typing import Any, Callable, Optional

from PyQt6.QtCore import QObject, QThread, pyqtSignal

from .ai_provider import (
    AIProvider, AIProviderConfig, AIProviderError, AIProviderFactory,
    PROVIDER_ANTHROPIC, DEFAULT_MODELS,
)
from .ai_log import AILogBus

log = logging.getLogger("autoic.ai")


# ---------------------------------------------------------------------------
# Errors
# ---------------------------------------------------------------------------
class AIEngineError(Exception):
    """Raised when the AI engine cannot complete a request."""


class AIParseError(AIEngineError):
    """Raised when the model output cannot be parsed."""


